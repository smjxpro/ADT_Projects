St. Augustine

Augustine of Hippo (/ɔːˈɡʌstɪn/; 13 November 354 – 28 August 430 AD) was a Roman African, Manichaean, early Christian theologian, doctor of the Church, and Neoplatonic philosopher from Numidia (Numidia -modern-day Souk Ahras, Algeria) whose writings influenced the development of the Western Church and Western philosophy, and indirectly all of Western Christianity. He was the bishop of Hippo Regius in North Africa and is viewed as one of the most important Church Fathers of the Latin Church for his writings in the Patristic Period. Among his most important works are The City of God, De doctrina Christiana, and Confessions. 

CITY OF GOD

On the city of God against the pagans (Latin: Dē cīvitāte Deī contrā pāgānōs), often called The City of God, is a book of Christian philosophy written in Latin by Augustine of Hippo in the early 5th century AD. The book was in response to allegations that Christianity brought about the decline of Rome and is considered one of Augustine's most important works, standing alongside The Confessions, The Enchiridion, On Christian Doctrine, and On the Trinity. As a work of one of the most influential Church Fathers, The City of God is a cornerstone of Western thought, expounding on many profound questions of theology, such as the suffering of the righteous, the existence of evil, the conflict between free will and divine omniscience, and the doctrine of original sin. 

BACKGROUND

The sack of Rome by the Visigoths in 410 left Romans in a deep state of shock, and many Romans saw it as punishment for abandoning traditional Roman religion for Christianity. In response to these accusations, and in order to console Christians, Augustine wrote The City of God as an argument for the truth of Christianity over competing religions and philosophies. He argues that Christianity was not responsible for the Sack of Rome, but instead responsible for its success. Even if the earthly rule of the Empire was imperiled, it was the City of God that would ultimately triumph. Augustine's focus was Heaven, a theme of many Christian works of Late Antiquity. Despite Christianity's designation as the official religion of the Empire, Augustine declared its message to be spiritual rather than political. Christianity, he argued, should be concerned with the mystical, heavenly city, the New Jerusalem, rather than with earthly politics. 

The book presents human history as a conflict between what Augustine calls the Earthly City (often colloquially referred to as the City of Man, but never by Augustine) and the City of God, a conflict that is destined to end in victory for the latter. The City of God is marked by people who forego earthly pleasure to dedicate themselves to the eternal truths of God, now revealed fully in the Christian faith. The Earthly City, on the other hand, consists of people who have immersed themselves in the cares and pleasures of the present, passing world. 

Augustine’s thesis depicts the history of the world as universal warfare between God and the Devil. This metaphysical war is not limited by time but only by geography on Earth. In this war, God moves (by divine intervention, Providence) those governments, political/ideological movements and military forces aligned (or aligned the most) with the Catholic Church (the City of God) in order to oppose by all means—including military—those governments, political/ideological movements and military forces aligned (or aligned the most) with the Devil (the City of the World). 

This concept of world history guided by Divine Providence in a universal war between God and the Devil is part of the official doctrine of the Catholic Church as most recently stated in the Second Vatican Council's Gaudium et Spes document: "The Church ... holds that in her most benign Lord and Master can be found the key, the focal point and the goal of man, as well as of all human history ... all of human life, whether individual or collective, shows itself to be a dramatic struggle between good and evil, between light and darkness ... The Lord is the goal of human history the focal point of the longings of history and of civilization, the center of the human race, the joy of every heart and the answer to all its yearnings." 

SUMMARY 

The City of God, philosophical treatise vindicating Christianity written by the medieval philosopher Saint Augustine as De civitate Dei about 413–426 ce. A masterpiece of Western culture, The City of God was written in response to pagan claims that the sack of Rome by barbarians in 410 was one of the consequences of the abolition of pagan worship by Christian emperors. St. Augustine responded by asserting, to the contrary, that Christianity saved the city from complete destruction and that Rome’s fall was the result of internal moral decay. He further outlined his vision of two societies, that of the elect (“The City of God”) and that of the damned (“The City of Man”). These “cities” are symbolic embodiments of the two spiritual powers—faith and unbelief—that have contended with each other since the fall of the angels. They are inextricably intermingled on this earth and will remain so until time’s end. St. Augustine also developed his theological interpretation of human history, which he perceives as linear and predestined, beginning with creation and ending with the Second Coming of Christ. 

The City of God is a religious, political, and philosophical dissertation on the fall of Rome. In this work, divided into twenty-two books, Augustine argues against claims that Christianity caused Rome to fall as he addresses the social and political climate of Rome and events of the time (410 BCE). Augustine proposes that Christianity actually helped Rome survive.

Augustine describes the existence of two groups or sub-cities, "The City of God" (believers, the elect) and "The City of Man" (non-believers, pagans), at odds with one another in Rome and society in general. Augustine explains that since the fall of angels in the beginning of time, these two groups have contended for the souls of man everywhere, and these forces continue the fight over the hearts and souls of Romans.

Each of the twenty-two books addresses a spiritual aspect related to the condition of man in relation to God.

